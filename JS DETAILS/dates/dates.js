let date = new Date();
console.log(date.toString()); //output: Mon Aug 30 2021 12:15:20 GMT+0600 (Bangladesh Standard Time)
console.log(date.toDateString()); // output: Mon Aug 30 2021
console.log(date.getTime()); //returns miliseconds 
console.log(date.getFullYear()); //full year
console.log(date.getMonth()); // month er number 
console.log(date.getDate()); // today date
console.log(date.getDay()); //day: (Sunday=0, Monday=1, Tuesday=2 , Wednesday =3,Thursday =4,Friday =5,Saturday =6, )